#The following are Comparison operators: <, >, <=, >=, ==, and !=
SmallValue = 5
MediumValue = 50
LargeValue = 200
MimicValue = 50

print("This variable equals " + str(SmallValue) + " and is smaller than 10")
print('The expression is: SmallValue > 10')
print(SmallValue > 10)

print('')

print("This variable equals " + str(LargeValue) + " and is larger than or equal to 10")
print('The expression is: LargeValue >= 10')
print(LargeValue >= 10)

print("")

print("This variable equals " + str(MediumValue) + " and is equal to 50")
print('The expression is: MediumValue == MimicValue')
print(MediumValue == MimicValue)

print("")

print("This variable equals " + str(MediumValue) + " and is not equal to 300")
print('The expression is: MediumValue != 300')
print(MediumValue != 300)

print("")

print("In addition to Comparison operators are Boolean operators")
print("And statements require both sides to be true")
print("Or statements require one side to be true")
print("Not statements convert a true statement to a false output")
